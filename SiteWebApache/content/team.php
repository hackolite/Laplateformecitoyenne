<h2>L'équipe !</h2>
<p>
Quand les institutions ne sont pas en mesure de couvrir tous les besoins, La Plateforme Citoyenne compte sur chaque citoyen : Connectons-nous !
</p>
<p>Co-Fondateurs du collectif adresse93075, visant à connecter les usagers avec leurs territoires, Karima Lebsir et Selim Zouaoui ont décidé de créer la plateforme citoyenne. Loïc Loréote, co-fondateur du laboratoire éco-citoyen, la Paillasse, a très vite rejoint cette aventure.
</p>
<p>“Cette plateforme a pour but de créer du lien, entre des citoyens ayant des capacités de production d’ustensiles médicaux et ceux dans le besoin. Au vu de la crise sanitaireliée au COVID19 et du confinement nous imposant de rester chez nous, l'utilisation d'une carte interactive permet de travailler avec des acteurs locaux et d'éviter les déplacements. 
</p>
<p>Durant le développement de cette initiative citoyenne et a but non lucratif, nous avonsconstaté que ce processus pouvait palier à d'autres problèmes comme ceux liés à l'environnement, à l'habitat et au monde animalier. C'est pourquoi nous travaillons aujourd'hui à proposer plusieurs domaines d’actions aux futurs inscrits de la plateforme citoyenne.
</p>
<p>Tout au long de notre chemin, nous avons été rejoints par : Ménélick Bekolo, Morgane Pauron, Quentin Phillip, Julien Zhu, Yann Djoli, Oriane Rainero.”
